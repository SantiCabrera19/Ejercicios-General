package Informatorio.Etapa3_Java;

public class Ejercicio10_Jedi {
    public static void main(String[] args) {
        // Defino las variables
        int num1 = 100;

        // bucle for donde decremento en 1 20 veces el valor guardado en "num1"
        for(int i = 0; i < 20; i++){
            num1--;
        }
        
        // Muestro por pantalla/consola le resultado del decremento
        System.out.println(num1);
    }
}
